<b>제16조 (회사의 의무) </b><br/>
① 회사는 본 서비스 제공과 관련하여 수집한 회원의 개인정보를 해당 회원의 동의 없이 제3자에게 제공, 누설, 전송, 배포하지 않습니다. 단, 전기통신사업법, 통신비밀보호법, 정보통신망 이용촉진 및 정보보호 등에 관한 법률 등 관계 법령에 따라 권한 있는 자가 적법한 절차를 통해 개인정보의 제공을 요청할 때에는 그렇지 않습니다. <br/>
② 회사는 서비스의 개선, 고객 만족도 향상 등의 목적으로 회원의 사전 동의 없이 회원 전체 또는 일부의 개인정보에 관한 통계자료를 작성하여 이를 사용할 수 있습니다.<br/>
③ 회사는 본 서비스와 관련한 회원의 불만사항이 접수되면 이를 신속하게 처리해야 하며, 신속한 처리가 어려울 때에는 그 사유와 처리 일정을 홈페이지의 공지사항 또는 이동전화망을 통하여 구현된 서비스 화면에 게재하거나 문자메시지(SMS) 등으로 해당 회원에게 통지합니다.<br/>
④ 본 서비스로 말미암아 회원에게 손해가 발생하였을 때 그러한 손해가 회사의 고의나 과실에서 비롯되었다면 회사에서 책임을 부담하며, 그 책임의 범위는 통상손해에 한합니다. <br/>
⑤ 회사는 전기통신사업법, 통신비밀보호법, 정보통신망 이용촉진 및 정보보호 등에 관한 법률 등 본 서비스의 운영, 유지와 관련된 법규를 지킵니다.<br/>
<br/>

<b>제17조 (회원의 의무) </b><br/>
① 회원은 본 서비스를 이용할 때 다음에 해당하는 행위를 해서는 안 됩니다.<br/>
1. 이용 신청 또는 변경 때 거짓된 정보를 적거나 다른 회원의 이동전화번호를 무단으로 부정하게 사용하는 행위 <br/>
2. 본 서비스를 이용하여 얻은 회사의 정보를 회사의 사전 동의 없이 복제 또는 유통하거나 상업적으로 이용하는 행위<br/>
3. 회사의 지적 재산권 등 기타 권리를 침해하는 행위 <br/>
4. 본 서비스와 관련된 설비의 오동작이나 정보 등의 파괴 및 혼란을 유발하는 컴퓨터 바이러스 감염 자료를 등록 또는 유포, 연결(링크)하는 행위 <br/>
5. 본 서비스 운영을 고의로 방해하거나 본 서비스의 안정적 운영을 방해할 수 있는 정보 및 수신자의 명시적인 수신거부 의사를 거부하고 광고성 정보를 전송하는 행위 <br/>
6. 타인으로 가장하거나 타인과의 관계를 허위로 명시하는 행위 <br/>
7. 다른 회원의 개인정보를 수집, 저장, 공개하는 행위<br/>
8. 자기 또는 타인에게 재산상의 이익을 주거나 타인에게 손해를 끼칠 목적으로 거짓된 정보를 유통하는 행위<br/>
9. 기타 불법적이거나 부당한 행위 <br/>
② 회원은 관계 법령, 본 약관의 규정, 이용안내 및 본 서비스상에 공지한 주의사항, 회사가 통지하는 사항 등을 지켜야 하며, 기타 회사의 업무에 방해되는 행위를 해서는 안 됩니다. <br/>
③ 회원은 회사에서 공식적으로 인정한 경우 이외에는 본 서비스를 이용하여 상품을 판매하는 영업활동을 할 수 없으며, 특히 해킹, 광고를 통한 수익, 음란 사이트를 통한 영업활동의 결과 및 손실, 관계기관에 의한 구속 등 법적 조치 등에 관해 회사는 책임을 지지 않으며, 이러한 행위로 회사에 손해가 발생했을 때 회원은 회사에 대하여 손해배상 의무를 집니다. <br/>
④ 회원이 본 조 제1항에 명시된 행위를 했을 때 회사는 부가적으로 제공한 혜택의 일부 또는 전부의 회수, 특정 서비스의 이용제한, 이용계약의 해지, 손해배상의 청구 등의 조치를 할 수 있습니다. <br/>
⑤ 본 조 제4항에서 정한 조치를 할 때 회사는 해당 내용을 미리 회원에게 통보하며, 회원과 연락할 수 없거나 긴급을 요할 때에는 먼저 조치한 후에 통보할 수 있습니다. <br/>
⑥ 회원은 본 조 제4항에 따른 회사의 조치에 대해 항변 사유가 있으면 항변할 수 있습니다. <br/>
⑦ 본 조 제4항에 따라 회사가 회원과의 이용계약을 해지하더라도 해지 이전에 체결된 매매계약의 완결에 관해서는 본 약관이 계속 적용됩니다. <br/>
⑧ 본 조 제4항에서 정한 바에 따라 이용계약이 종료되면 회원의 재이용 신청을 회사는 승낙하지 않을 수 있습니다.<br/>
<br/>

<b>제18조 (회원에 대한 통지) </b><br/>
① 회원에게 통지할 때 회사는 해당 내용을 이동전화망을 통하여 구현된 서비스 초기 화면에 공지하거나 문자메시지(SMS) 등으로 회원에게 전달할 수 있습니다. <br/>
② 회사가 불특정 다수 회원에게 통지할 때는 이동전화망을 통하여 구현된 서비스 초기 화면에 7일 이상 게시하는 것으로 개별 통지를 대신할 수 있습니다.<br/>
<br/>

<b>제19조 (이용자의 개인정보 보호) </b><br/>
회사는 관련 법령이 정하는 바에 따라 회원 등록정보를 포함한 회원의 개인정보를 보호하기 위하여 노력합니다. 회원의 개인정보 보호와 관련된 사항은 관련 법령 및 회사가 정하는 '개인정보취급방침'에 정한 바를 따릅니다.<br/>
<br/>

<b>제20조 (개인정보의 제공, 위탁)</b><br/>
① 회사는 본 서비스의 제공을 위하여 관련 법령에 따라 회원의 개인정보를 제휴사, 가맹점 또는 위탁사 등에 제공할 수 있습니다. <br/>
② 회사는 수집된 개인정보의 취급 및 관리 업무(이하 '업무')를 스스로 수행함을 원칙으로 하나, 필요하면 업무의 일부 또는 전부를 회사가 선정한 회사에 위탁할 수 있습니다.<br/>
③ 회사는 회원의 정보를 본 서비스를 제공하기 위한 목적 이외의 용도로 사용하거나 회원의 동의 없이 제3자에게 제공하지 않습니다. 단, 다음의 경우는 예외로 합니다. <br/>
1. 배송에 필요한 최소한의 이용자의 정보(성명, 주소, 전화번호)를 배송업체에 알려주는 경우 <br/>
2. 통계작성, 학술연구, 시장조사에 필요한 경우로서 특정 개인을 식별할 수 없는 형태로 제공하는 경우<br/> 
3. 재화 등의 거래에 따른 대금정산을 위하여 필요한 경우 <br/>
4. 법령이 회원의 개인정보의 이용과 제3자에 대한 정보제공을 허용하는 경우<br/>
