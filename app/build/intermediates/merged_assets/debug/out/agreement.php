<b>제23조 (손해배상) </b><br/>
① 본 약관의 규정을 위반하여 회사에 손해를 끼친 회원은 회사에 발생한 모든 손해를 배상해야 합니다.<br/>
② 회원이 본 서비스를 이용하면서 불법 행위나 본 약관을 위반하는 행위를 하여 회사가 해당 회원 이외의 제3자로부터 손해배상 청구 또는 소송을 비롯한 각종 이의제기를 받는다면 해당 회원은 자신의 책임과 비용으로 회사를 면책시켜야 하며, 회사가 면책되지 못하면 회사에 발생한 모든 손해를 배상해야 합니다.<br/>
<br/> 

<b>제24조 (면책사항) </b><br/>
① 천재지변 또는 이에 준하는 불가항력으로 본 서비스를 제공할 수 없을 때 회사는 본 서비스 제공에 관한 책임이 면제됩니다. <br/>
② 회사는 회사의 귀책사유가 아닌 사유로 발생한 본 서비스의 이용장애에 대해 책임을 지지 않습니다. <br/>
③ 회사는 회원이 본 서비스를 이용하여 기대하는 수익을 달성하지 못했거나 상실한 것에 대해 책임을 지지 않으며 그 밖에 본 서비스를 통하여 얻은 정보로 인하여 발생한 손해 등에 대해서도 책임을 지지 않습니다. 회사는 회원이 사이트에 게재한 정보, 자료, 사실의 신뢰도 및 정확성 등에 대해 책임을 지지 않습니다.<br/>
④ 회사는 본 서비스를 매개로 회원 상호 간 또는 회원과 제3자 상호 간에 분쟁이 발생하였을 때 이에 개입할 의무가 없으며 분쟁에 따른 손해를 배상할 책임도 없습니다. <br/>
⑤ 회원이 자신의 개인정보를 타인에게 유출하거나 제공하여 발생하는 피해에 대해 회사는 일절 책임을 지지 않습니다. <br/>
⑥ 본 서비스 화면에서 링크, 배너 등을 통하여 연결된 회사(이하 ‘피연결회사’)와 회원 간에 이루어진 거래에 회사는 개입하지 않으며, 해당 거래에 대해 책임을 지지 않습니다.<br/>
<br/>

<b>제25조 (관할법원) </b><br/>
① 본 서비스 이용과 관련하여 회사와 회원 사이에 분쟁이 발생하면, 회사와 회원은 분쟁의 해결을 위해 성실히 협의합니다. <br/>
② 전 항의 협의에도 불구하고 분쟁이 해결되지 않으면, 회사와 회원 양 당사자는 민사소송법상의 관할법원에 소를 제기할 수 있습니다.<br/>
