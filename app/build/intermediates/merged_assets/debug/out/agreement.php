<b>제 1 조 (목적)</b><br/>
본 약관은 회원이 ㈜비타(이하 ‘회사’)에서 제공하는 simdori 서비스를 이용하는 것과 관련하여 회원과 회사 간의 권리, 의무 및 책임사항, 서비스 이용에 따른 이용조건 및 절차 등 기본적인 사항을 규정함을 목적으로 합니다.<br/>
<br/>

<b>제 2 조 (약관의 효력 및 변경)</b><br/>
① 회사는 이 약관의 내용을 회원이 쉽게 알 수 있도록 서비스 초기 화면에 게시합니다.<br/>
② 회사는 전자상거래 등에서의 소비자보호에 관한 법률, 약관의 규제에 관한 법률, 소비자기본법 등 관련법을 위배하지 않는 범위에서 이 약관을 개정할 수 있습니다.<br/>
③ 회사가 약관을 개정할 경우에는 기존약관과 개정약관 및 개정약관의 적용일자와 개정사유를 명시하여 현행약관과 함께 그 적용일자 15일 전부터 적용일 이후 상당한 기간 동안, 개정 내용이 회원에게 불리한 경우에는 그 적용일자 30일 전부터 적용일 이후 상당한 기간 동안 각각 이를 서비스 초기 화면에 공지하거나 회원가입시 회원이 입력한 이메일 주소로 개정약관을 발송하여 고지합니다. <br/>
④ 회사가 전항에 따라 회원에게 통지하면서 공지∙고지일로부터 개정약관 시행일 7일 후까지 거부의사를 표시하지 아니하면 승인한 것으로 본다는 뜻을 명확하게 고지하였음에도 의사표시가 없는 경우에는 변경된 약관을 승인한 것으로 봅니다. 회원이 개정약관에 동의하지 않을 경우 회원은 ‘simdori’ 서비스의 이용을 중단하고 이용 계약을 해지할 수 있습니다.<br/>
<br/>

<b>제 3 조 (약관 외 준칙)</b><br/>
① 본 약관에 밝히지 않은 사항은 전기통신기본법, 전기통신사업법, 정보통신망 이용촉진 및 정보보호 등에 관한 법률 등 관계 법령 및 회사가 정한 본 서비스 세부이용지침 등의 규정을 따릅니다.<br/>
② 회사는 필요한 경우 특정 서비스에 관하여 적용될 사항(이하 ‘개별약관’이라 함)을 정하여 이를 제2조 제 2항의 방법에 의하여 공지할 수 있습니다.<br/>
<br/>

<b>제 4 조 (용어의 정의)</b><br/>
① 본 약관에서 사용하는 용어의 정의는 다음과 같습니다.<br/>
1. ‘simdori’(이하 ‘본 서비스’)란 회원과 제휴사 간에 개별적으로 체결된 계약에 근거하여 회원이 취득한 제휴사의 쿠폰 등을 이용, 관리하거나 타인에게 공유가 가능하며 회사가 제공하는 회원이 미리 주문하여 바로 찾아갈 수 있도록 하는 모바일 서비스를 말합니다. <br/>
2. ‘회원’이란 이동전화 서비스 이용자가 본 서비스 애플리케이션을 설치, 본 서비스 이용약관에 동의하여 가입을 완료하고 본 서비스를 이용할 수 있는 권한을 취득한 자를 말합니다.</br>
3. ‘제휴사’란 회사와의 계약하에 회원에게 본 서비스를 통하여 쿠폰 등을 제공하는 회사를 말합니다.<br/>
4. ‘운영자’란 본 서비스의 전반적인 관리와 원활한 운영을 위하여 회사가 선정한 자를 말합니다.<br/>
5. ‘메뉴’란 제휴사가 회원에게 판매하는 모든 것을 말한다.<br/>
