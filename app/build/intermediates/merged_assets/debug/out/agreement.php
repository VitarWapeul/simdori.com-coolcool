<b>제21조 (계약해지 및 이용제한)</b><br/>
① 회원은 다음에서 정한 바에 따라 이용계약을 해지할 수 있습니다.<br/>
1. 회원은 언제든지 회사에 이용계약 해지의 의사를 통지하여 이용계약을 해지할 수 있습니다. 단, 해지의사를 통지하기 전에 현재 진행 중인 모든 주문을 완료하거나 취소하여야 하며, 거래를 취소했을 때 발생할 수 있는 불이익은 회원 본인이 부담하여야 합니다. <br/>
2. 회원 탈퇴로 인해 발생한 불이익에 대한 책임은 회원 본인이 져야 하며, 이용계약이 종료되면 회사는 회원에게 부가적으로 제공한 각종 혜택을 회수할 수 있습니다.<br/>
3. 임의 해지, 재가입 등을 반복적으로 행하여 회사가 제공하는 할인쿠폰, 이벤트 혜택 등의 경제적 이익을 취하거나 이 과정에서 타인의 명의를 무단으로 사용하는 등 불법과 편법 행위를 하는 회원을 차단하기 위하여 회사는 회원의 회원탈퇴 후 3개월 동안 해당 회원의 아이디(ID), 성명, 본인확인기관을 통해 받는 연계정보(CI), 중복가입 식별정보(DI), 탈퇴일자, 탈퇴사유 등 개인정보를 보관합니다.<br/>
② 회사는 회원이 제17조에 규정한 회원의 의무를 이행하지 않으면 사전 통지 없이 즉시 이용계약을 해지하거나 본 서비스의 제공을 중지할 수 있습니다. <br/>
③ 회사는 회원 가입 후 6개월 동안 본 서비스를 사용한 적이 없는 회원에게 서비스를 계속 사용할지를 묻고, 회사가 정한 기한 내에 그에 대한 답변이 없으면 이용 계약을 해지할 수 있습니다. <br/>
④ 본 조 제2항과 제3항에 규정된 회사의 조치에 대해 회원은 회사가 정한 절차에 따라 이의신청을 할 수 있습니다. <br/>
⑤ 전 항의 이의가 정당하다고 회사가 인정하면, 회사는 즉시 본 서비스의 이용을 재개합니다.<br/>
<br/>

<b>제22조 (양도 금지)</b><br/>
회원은 본 서비스의 이용권한 등 이용 계약상 지위를 타인에게 양도, 증여할 수 없으며, 담보로 제공할 수 없습니다.<br/>
