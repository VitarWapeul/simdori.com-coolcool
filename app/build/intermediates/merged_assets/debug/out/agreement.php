<b>제 9 조 (서비스의 이용 개시)</b><br/>
① 회사는 이용자가 본 서비스의 모바일 어플리케이션을 본인 명의의 단말기에 다운로드 및 설치한 뒤 이용약관에 동의함으로써 본 서비스의 제공을 게시합니다.<br/>
② 회사의 업무상 또는 기술상의 장애로 본 서비스의 제공을 개시하지 못할 때에는 해당 내용을 회원에게 통지합니다.<br/>
③ 회원은 본 서비스 가입 후 제휴사에서 발급받은 쿠폰을 사용하거나 선물로 주고받은 쿠폰을 확인, 사용, 관리할 수 있습니다.<br/> 
<br/>

<b>제 10 조 (대리행위 및 보증의 부인)</b><br/>
① 회사는 회원과 제휴사 간의 자유로운 서비스의 매개를 위한 시스템을 운영 및 관리, 제공할 뿐이므로 회원 또는 제휴사를 대리하지 않으며, 회원과 제휴사 또는 회원과 회원 사이에 성립된 계약과 관련된 책임과 회원이 제공한 정보에 대한 책임은 해당 회원이 직접 부담하여야 합니다.<br/>
② 회사는 본 서비스를 통하여 이루어지는 회원과 제휴사 간의 계약과 관련하여 체결 의사의 존재 여부, 및 전정성, 계약 내용 및 조건의 완전성, 안정성, 적법성 및 타인의 권리에 대한 비침해성, 회원 또는 제휴사가 입력하는 정보의 진실성 또는 적법성 등 일체에 대하여 보증하지 않으며, 이와 관련한 일체의 위험과 책임은 해당 회원 또는 제휴사가 전적으로 부담합니다.<br/>
③ 회원은 메뉴를 주문하기 전에 반드시 메뉴 주문에 대한 상세 내용을 정확하게 확인한 후 주문해야 합니다. 한편, 주문의 환불 등 제반 문제는 회원과 제휴사 간의 문제이므로 회사는 이에 관여하지 않습니다. 다만, 회사는 회원과 제휴사 간의 거래와 관련하여 통신판매중개자로서 ‘전자상거래 등에서의 소비자보호에 관한 법률’ 등 관련 법령에서 정한 책임은 부담합니다.<br/>
<br/>

<b>제 11 조 (서비스의 이용시간)</b><br/>
① 본 서비스의 이용시간은 연중무휴 1일 24시간을 원칙으로 합니다. 다만, 회사의 업무상이나 기술상의 이유로 또는 이동전화망이나 인터넷망의 불안정이나 기술상의 이유로 본 서비스가 일시 중지될 수 있으며, 운영상의 필요성으로 회사가 정한 기간에 본 서비스가 일시 중지될 수 있습니다.<br/>
② 회사는 본 서비스를 일정 범위로 나누어 이 범위별로 이용할 수 있는 시간을 별도로 정할 수 있으며 이때 그 내용을 공지합니다.<br/>
<br/>

<b>제12조 (서비스의 변경 및 중지) </b><br/>
① 회사는 본 서비스를 변경하여 제공할 수 있습니다. 이때 회사는 변경될 서비스의 내용과 제공일자를 제18조에서 정한 방법으로 회원에게 공지하거나 통지합니다. <br/>
② 회사는 다음에 해당하면 본 서비스의 전부 또는 일부를 제한하거나 중지할 수 있습니다.<br/>
1. 서비스용 설비의 보수 등 공사로 부득이한 경우 <br/>
2. 회원이 회사의 본 서비스 운영을 방해하는 경우 <br/>
3. 정전, 제반 설비의 장애 또는 이용량의 폭주 등으로 정상적인 서비스 이용에 지장이 있는 경우 <br/>
4. 본 서비스의 변경 및 중단 등과 같은 특수한 사정으로 본 서비스를 유지할 수 없는 경우<br/>
5. 기타 천재지변, 국가비상사태 등 불가항력 사유가 있는 경우<br/>
③ 전 항에 따라 본 서비스를 중단할 때에는 회사는 제18조에서 정한 방법으로 회원에게 통지합니다. 다만, 회사가 통제할 수 없는 사유로 본 서비스를 중단(회사 또는 운영자의 고의.과실이 없는 디스크 장애, 시스템 다운 등)하게 되어 미리 통지할 수 없었을 때에는 그렇지 않습니다.<br/>
④ 회사는 고의 또는 중과실이 없는 한 본 조에 따른 본 서비스의 변경, 중지로 발생하는 문제에 대해 어떠한 책임도 지지 않습니다.<br/>
 <br/>

<b>제13조 (정보의 제공 및 광고의 게재) </b><br/>
① 회사는 본 서비스의 운영과 관련한 공지사항을 본 서비스의 이동전화망을 통하여 구현된 모바일 애플리케이션의 서비스 초기 화면에 공지하거나 문자메시지(SMS), 이메일(E-mail), 스마트푸시(Smart Push)등으로 통지할 수 있습니다.<br/>
② 회사는 본 서비스의 운영과 관련한 각종 정보와 광고를 본 서비스의 홈페이지 또는 이동전화망을 통하여 구현된 모바일 애플리케이션의 서비스 화면에 게재하거나 회원의 동의를 얻어 이메일(E-mail), 우편, 단문/장문 문자메시지(SMS/MMS), 텔레마케팅(TM) 등의 방법으로 회원에게 제공할 수 있습니다.<br/>
③ 회원이 본 서비스상에 게재된 광고를 이용하거나 본 서비스를 통한 광고주의 판촉활동에 참여하여 광고주와 교신하거나 거래하는 것은 전적으로 회원과 광고주 간의 문제입니다. 따라서 회원과 광고주 간에 분쟁이 발생하면 회원과 광고주가 직접 해결해야 하며, 이와 관련하여 회사는 어떠한 책임도 지지 않습니다.<br/>
 <br/>

<b>제14조 (게시물 또는 내용물의 게시 등 중단) </b><br/>
① 회사는 회원이 게시하거나 전달하는 모든 게시물이 다음에 해당한다고 판단되면 사전 통지 없이 게시, 전달 등을 중단할 수 있으며, 이에 대해 회사는 어떠한 책임도 지지 않습니다. <br/>
1. 회사, 다른 회원 또는 제3자를 비방하거나 중상모략으로 명예를 손상하는 경우<br/>
2. 공공질서와 미풍양속을 거스르는 내용의 게시물을 유포하는 경우 <br/>
3. 범죄 행위에 결부된다고 인정되는 경우 <br/>
4. 회사 또는 제3자의 저작권 등 기타 권리를 침해하는 경우<br/>
5. 불필요하거나 승인되지 않은 광고, 판촉물을 게재하는 경우<br/>
6. 기타 관계 법령과 회사의 지침 등을 위반하는 것으로 판단되는 경우<br/>
② 회사는 게시물과 관련된 세부이용지침을 별도로 정하여 시행할 수 있으며, 회원은 그 지침에 따라 각종 게시물을 등록하거나 삭제하여야 합니다.<br/>
<br/>

<b>제15조(게시물의 지적재산권) </b><br/>
① 회원이 본 서비스 내에 게시한 게시물에 대한 저작권 및 관련 책임은 해당 회원에게 있으며 회사는 본 서비스의 활성화를 위하여 회원의 게시물을 서비스 내에 게시하거나 활용할 수 있습니다. <br/>
② 회사는 회원이 본 서비스 내에 게시한 게시물이 타인의 저작권 등 지적 재산권을 침해하더라도, 이에 대한 민.형사상의 책임을 지지 않습니다. 만일 회원이 타인의 저작권 등 지적 재산권을 침해하여 회사가 타인으로부터 손해배상 청구 등 이의제기를 받는다면 회원은 회사의 면책을 위하여 노력해야 하며, 회사가 면책되지 못할 때에는 회사에 발생한 모든 손해를 부담해야 합니다.<br/>
③ 회사는 게시한 회원의 동의 없이 게시물을 본 서비스 내 이용 외에 다른 목적으로 사용할 수 없습니다. 단, 회사의 본 서비스를 홍보할 목적으로 미디어, 통신사 등에 이용자의 게시물 내용을 보도 및,방영하게 할 때에는 회원에게 미리 동의를 구합니다.<br/>
④ 회사는 회원이 탈퇴하거나 적법한 사유로 해지되면 해당 회원이 게시하였던 게시물을 삭제할 수 있습니다.<br/>
⑤ 회사가 작성한 저작물에 대한 저작권은 회사에 귀속됩니다.<br/>
⑥ 회원은 본 서비스를 이용하여 얻은 정보를 가공 및 판매하는 행위 등 본 서비스에 게재된 자료를 영리 목적으로 이용하거나 제3자에게 이용하게 할 수 없으며, 게시물에 대한 저작권 침해는 관계 법령의 적용을 받습니다.<br/>
