<b>제 5 조 (회원가입절차)</b><br/>
① 서비스 이용자가 본 약관을 읽고 “동의” 버튼을 누르거나 “확인” 등에 체크하는 방법을 취한 경우 본 약관에 동의한 것으로 간주합니다.<br/>
② 본 서비스 이용을 위한 회원가입은 서비스 이용자가 제1항과 같이 동의한 후, 회사가 정한 회원가입 신청서에 회원 ID를 포함한 필수사항을 입력하고, ‘다음단계’ 버튼을 누르는 방법으로 진행합니다. <br/>
③ 본 서비스 이용을 위한 회원가입의 ‘다음단계’ 절차는 서비스 이용자의 휴대폰 번호 인증을 통한 인증절차입니다. <br/>
④ 인증절차가 완료되고 ‘가입완료’ 버튼을 누르면 본 서비스 이용을 위한 회원가입이 완료됩니다.<br/>
<br/>

<b>제 6 조 (회원등록의 승낙 및 제한)</b><br/>
① 회원등록은 제5조에 정한 절차에 의한 서비스 이용자의 회원가입 신청과 회사의 회원등록 승낙에 의하여 성립합니다. 회사는 회원가입 신청자가 필수사항 등을 성실히 입력하여 가입신청을 완료하였을 때에는 필요한 사항을 확인한 후 지체 없이 이를 승낙을 하여야 합니다. <br/>
② 회사가 이용자의 이용 신청을 승낙하는 동시에 이용자는 회사가 제공하는 본 서비스의 이용자가 되며, 본 서비스의 이용에 따른 별도의 비용이 들어가게 되면 회사는 이용자에게 이를 알립니다.<br/>
③ 회사는 다음에 해당하는 이용자의 이용 신청에 대하여는 승낙을 하지 않을 수 있습니다.<br/>
1. 타인 명의로 신청한 경우 승낙할 수 없습니다.<br/>
2. 입력한 정보의 내용이 허위인 경우 승낙할 수 없습니다.<br/>
3. 대포폰(이동전화 서비스 본래의 목적과는 달리 불법대출 등 부정 사용을 목적으로 타인 명의를 무단으로 사용하여 개통하거나 명의자를 속여 개통하는 휴대전화)를 이용하는 경우 승낙할 수 없습니다.<br/>
4. 타인의 명의를 무단으로 사용한 사실이 있거나 명의를 무단으로 사용한 이유로 처벌받은 경우 승낙할 수 없습니다.<br/>
5. 불법 복제와 관련된 사실이 있거나 그런 사유로 처벌받은 경우 승낙할 수 없습니다.<br/>
<br/>

<b>제 7 조 (회원 ID 등의 관리책임)</b><br/>
① 회원은 서비스 이용을 위한 회원 ID, 비밀번호의 관리에 대한 책임, 본인 ID의 제3자에 의한 부정사용 등 회원의 고의∙과실로 인해 발생하는 모든 불이익에 대한 책임을 부담합니다. 단, 이것이 회사의 고의∙과실로 인하여 야기된 경우는 회사가 책임을 부담합니다.<br/>
② 회원은 회원 ID, 비밀번호 및 추가정보 등을 도난 당하거나 제3자가 사용하고 있음을 인지한 경우에는 즉시 본인의 비밀번호를 수정하는 등의 조치를 취하여야 하며 즉시 이를 회사에 통보하여 회사의 안내를 따라야 합니다.<br/>
<br/>

<b>제 8 조 (개인정보의 수집 등)</b><br/>
회사는 서비스를 제공하기 위하여 관련 법령의 규정에 따라 회원으로부터 필요한 개인정보를 수집합니다. <br/>
