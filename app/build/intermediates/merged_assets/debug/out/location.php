<b>제10조 (고객의 개인위치정보보호)</b><br/>
회사는 관련법령이 정하는 바에 따라서 고객의 개인위치정보를 보호하기 위하여 노력합니다.<br/>
<br/>

<b>제11조 (개인위치정보의 이용 또는 제공)</b><br/>
① 회사는 서비스 제공을 위하여 고객의 위치정보를 이용할 수 있으며, 고객은 본 약관에 동의함으로써 이에 동의한 것으로 간주됩니다.<br/>
② 회사는 고객이 제공한 개인위치정보를 당해 고객의 동의 없이 서비스 제공 이외의 목적으로 이용하지 아니합니다.<br/>
③ 회사는 고객이 제공한 개인위치정보를 당해 고객의 동의 없이 제3자에게 제공하지 아니합니다.<br/>
④ 회사는 고객의 개인위치정보를 고객이 지정하는 제3자에게 제공할 경우 매회 고객에게 제공받는 자, 제공 일시 및 제공목적을 즉시 통보합니다.<br/>
⑤ 다만, 회사는 고객이 미리 요청한 경우에 한하여 고객이 지정한 이동전화 단말기 또는 전자우편주소로 통보할 수 있습니다.<br/>
1. 개인위치정보를 수집한 당해 통신단말장치가 문자, 음성 또는 영상의 수신기능을 갖추지 아니한 경우<br/>
2. 개인위치정보 주체가 개인위치정보를 수집한 당해 통신단말장치 외의 통신단말장치 또는 전자우편 주소 등으로 통보할 것을 미리 요청한 경우<br/>
<br/>

<b>제12조 (개인위치정보 이용/제공사실 확인자료의 보유)</b><br/>
① 회사는 위치정보법 제16조 제2항에 근거하여 고객에 대한 위치정보 이용/제공사실 확인자료를 위치정보시스템에 자동으로 기록하며, 고객불만 응대를 위하여 기록시점으로부터 6개월간 보존합니다.<br/>
② 회사는 위치정보법 제24조 제4항의 규정에 의하여 고객이 동의의 전부 또는 일부를 철회한 경우에는 지체 없이 수집된 개인위치정보 및 위치정보 이용제공사실 확인자료(동의의 일부를 철회하는 경우에는 철회하는 부분의 개인위치정보 및 위치정보 이용제공사실 확인자료에 한합니다)를 파기합니다. 다만, 국세기본법, 법인세법, 부가가치세법 기타 관계법령의 규정에 의하여 보존할 필요성이 있는 경우에는 관계법령에 따라 보존합니다.<br/>
<br/>

<b>제13조 (개인위치정보의 보유기간 및 이용기간)</b><br/>
회사는 고객의 개인위치정보를 이용한 경우 위치정보법 제16조 제2항의 규정에 의하여 기록 보존하여야 하는 위치정보이용 제공사실 확인자료 외의 당해 개인위치정보를, 위치정보법 제23조에 따라 고객이 동의한 목적범위 내에서의 이용 및 고객불만 응대를 위하여 6개월간 보유하며 6개월이 지난 경우 지체 없이 파기합니다.<br/>
<br/>

<b>제14조 (법정 대리인의 권리)</b><br/>
① 회사는 14세 미만의 아동으로부터 위치정보법 제18조 제1항, 제19조 제1항 및 제2항 또는 제21조의 규정에 의하여 개인위치정보를 이용 또는 제공하고자 하는 경우에는 그 법정대리인의 동의를 얻어야 합니다.<br/>
② 제17조(고객의 권리)의 규정은 위치정보법 제25조 제2항의 규정에 의하여 법정대리인이 동의를 하는 경우에 이를 준용합니다. 이 경우, 법정대리인을 고객으로 봅니다.<br/>
<br/>

<b>제15조 (8세 이하의 아동 등의 보호를 위한 위치정보 이용)</b><br/>
① 회사는 다음에 해당하는 자(이하 "8세 이하의 아동" 등이라 합니다)의 보호의무자가 8 세 이하의 아동 등의 생명 또는 신체보호를 위하여 개인위치정보의 수집, 이용 또는 제공에 동의하는 경우에는 본인의 동의가 있는 것으로 봅니다.<br/>
1. 8세 이하의 아동<br/>
2. 금치산자<br/>
3. 장애인복지법 제2조 제2항 제2호의규정에 의한 정신적 장애를 가진 자로서 장애인 고용촉진 및 직업재활법 제2조 제2호의 규정에 의한 중증장애인에 해당하는 자(장애인복지법 제29조의 규정에 의하여 장애인등록을 한 자에 한합니다.)<br/>
② 제1항의 규정에 의한 8세 이하의 아동 등의 보호의무자는 8세 이하의 아동 등을 사실상 보호하는 자로서 다음 각 호의 1에 해당하는 자를 말합니다.<br/>
1. 8세 이하의 아동의 법정대리인 또는 보호시설에 있는 미성년자의 후견직무에 관한 법률 제3조의 규정에 의한 후견인<br/>
2. 금치산자의 법정대리인<br/>
3. 본 조 제1항 제3호의 자의 법정대리인 또는 「장애인복지법」 제58조제1항 제1호의 규정에 의한 장애인생활시설(국가 또는 지방자치단체가 설치.운영하는 시설에 한합니다)의 장, 정신보건법 제3조제4호의 규정에 의한 정신질환자 사회복귀시설(국가 또는 지방자치단체가 설치.운영하는 시설에 한합니다)의 장, 정신보건법 제3조 제5호의 규정에 의한 정신요양시설의 장<br/>
③ 8세 이하의 아동 등의 생명 또는 신체의 보호를 위하여 개인위치정보의 수집/이용 또는 제공에 동의하고자 하는 보호의무자는 서명동의서에 보호의무자임을 증명하는 서류를 첨부하여 회사에 제출하여야 합니다.<br/>
④ 제17조(고객의 권리)의 규정은 위치정보법 제26조 제4항의 규정에 의하여 보호의무자가 동의를 하는 경우에 이를 준용합니다. 이 경우, 보호의무자를 고객으로 봅니다.<br/>
