<b>제1조 (목적)</b><br/>
이 약관은 ㈜와플(이하 “회사”라 합니다)가 제공하는 위치기반서비스(이하 ‘서비스’라 합니다)에 관하여 회사와 이용계약을 체결한 고객이 서비스를 이용함에 있어 필요한 회사와 고객의 권리 및 의무, 기타 제반 사항을 정함을 목적으로 합니다.<br/>
<br/>

<b>제2조 (약관 외 준칙)</b><br/>
이 약관에 명시되지 않은 사항에 대해서는 위치정보의 보호 및 이용 등에 관한 법률(이하 ‘위치정보법’이라 합니다), 전기통신사업법, 정보통신망 이용촉진 및 보호 등에 관한 법률(이하 ‘정보통신망법’이라 합니다) 개인정보보호법 등 관계법령 및 회사가 정한 서비스의 세부이용지침 등의 규정에 따릅니다.<br/>
