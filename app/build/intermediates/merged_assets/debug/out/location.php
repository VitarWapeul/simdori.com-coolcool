<b>제16조 (회사의 의무)</b><br/>
① 회사는 서비스와 관련한 고객의 불만사항이 접수되는 경우 이를 신속하게 처리하여야 하며, 신속한 처리가 곤란한 경우 그 사유와 처리 일정을 고객에게 통지합니다.<br/>
② 회사는 위치정보의 보호 및 이용 등에 관한 법률, 정보통신망 이용촉진 및 정보보호 등에 관한 법률, 통신비밀보호법, 전기통신사업법, 개인정보보호법 등 서비스의 운영, 유지와 관련 있는 법규를 준수합니다.<br/>
③ 회사는 고객서비스를 보다 활성화하여 최적화된 서비스를 제공하고, 신상품이나 이벤트 정보안내, 설문조사 등 고객 지향적인 마케팅을 수행하기 위해 이용계약 체결 시 수집한 고객의 개인정보 및 서비스 이용과 관련한 정보를 활용할 수 있습니다. 단, 고객의 동의 범위를 초과하여 이용하거나 제3자에게 제공하고자 하는 경우에는 미리 당해 고객에게 동의를 받아야 합니다. 이 경우 고객은 회사의 동의 요청을 거절할 수 있습니다.<br/>
④ 회사는 요금연체와 관련하여 이용자를 신용정보의 이용 및 보호에 관한 법률 제17조의 신용정보집중기관 등 관계기관 등에 신용 불량자로 등록 요청할 경우 등록요청 대상자에 대해 본인여부 등 필요한 확인절차를 거쳐야 합니다.<br/>
<br/>

<b>제17조 (고객의 권리)</b><br/>
① 고객은 유무선인터넷 등을 통해 회사의 고객 개인위치정보의 이용 및 제공에 대한 동의의 전부 또는 일부에 대하여 철회할 수 있습니다.<br/>
② 고객은 유무선인터넷 등을 통해 회사의 고객 개인위치정보의 이용 및 제공의 일시적인 중지를 요구할 수 있습니다.<br/>
③ 고객은 회사에 대하여 다음 각 호의 1의 자료 등의 열람 또는 고지를 요구할 수 있고 그 자료에 오류가 있을 경우에 정정을 요구할 수 있습니다<br/>
1. 고객에 대한 위치정보 수집, 이용, 제공사실 확인자료<br/>
2. 고객의 개인위치정보가 위치정보법 또는 다른 법률의 규정에 의하여 제3자에게 제공된 이유 및 내용<br/>
<br/>

<b>제18조 (고객의 의무)</b><br/>
① 고객은 서비스이용계약에 따라 요금을 지정된 기일까지 납입하여야 하며, 회사에 알린 요금청구 주소 및 연락처가 변경된 경우에는 이를 회사에 알려야 합니다.<br/>
② 고객은 서비스 이용을 위해 필요한 제반 정보를 제공, 등록할 경우 현재의 사실과 일치하는 완전한 정보를 제공, 등록하여야 하며, 변경사항이 발생할 경우 즉시 갱신하여야 합니다.<br/>
③ 고객은 단말기가 정상 동작을 유지하도록 관리하여야 하며, 단말기의 정상동작이 유지되지 않아 서비스 제공에 지장이 있을 때에는 해당 단말기의 보수, 교환 등의 처리를 신속히 행하여 원활한 서비스가 이루어지도록 합니다.<br/>
④ 고객은 서비스 이용 시 다음 각 호의 1에 해당하는 행위를 하여서는 아니 됩니다.<br/>
1. 가입신청 또는 변경 시 허위 사실을 기재하는 행위<br/>
2. 타인의 명예를 손상시키거나 불이익을 주는 행위<br/>
3. 공공질서 및 미풍양속에 위반되는 내용의 정보, 문장, 도형, 음성 등을 타인에게 유포하는 행위<br/>
4. 서비스와 관련된 설비의 오동작이나 정보 등의 파괴 및 혼란을 유발시키는 컴퓨터 바이러스 감염 자료를 등록 또는 유포하는 행위<br/>
5. 타인으로 가장하는 행위 및 타인과의 관계를 허위로 명시하는 행위<br/>
6. 자기 또는 타인에게 재산상의 이익을 주거나 타인에게 손해를 가할 목적으로 허위의 정보를 유통시키는 행위<br/>
7. 서비스의 이용과 관련하여 취득한 타인의 개인위치정보를 무단으로 유용 또는 유출하는 행위<br/>
8. 기타 불법적이거나 부당한 행위<br/>
⑤ 고객은 관계 법령, 이 약관의 규정, 이용안내 및 서비스상에 공지한 주의사항, 회사가 통지하는 사항 등을 준수하여야 하며, 기타 회사의 업무에 방해되는 행위를 하여서는 아니 됩니다.<br/>
