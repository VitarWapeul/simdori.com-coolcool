<b>제3조 (가입자격)</b><br/>
① 서비스에 가입할 수 있는 자는 위치기반서비스를 이용할 수 있는 이동전화 단말기 기타 서비스를 이용할 수 있는 단말기(이하 ‘단말기’라 합니다)의 소유자 본인 입니다.<br/>
② 법인의 경우에는 법인의 사업자번호를 사용하여 서비스에 가입할 수 있습니다.<br/>
<br/>

<b>제4조 (서비스 가입)</b><br/>
① 회사가 정한 본 약관에 고객이 동의하고, 회사가 승낙함으로써 서비스 가입의 효력이 발생합니다.<br/>
② 회사는 다음 각 호의 고객 가입신청에 대해서는 이를 승낙하지 아니할 수 있습니다.<br/>
1. 실명이 아니거나 다른 사람의 명의를 사용하는 등 허위로 신청하는 경우<br/>
2. 고객 등록 사항을 누락하거나 오기하여 신청하는 경우<br/>
3. 공공질서 또는 미풍양속을 저해하거나 저해할 목적으로 신청한 경우<br/>
4. 기타 회사가 정한 이용신청 요건이 충족되지 않았을 경우<br/>
<br/>

<b>제5조 (서비스의 해지)</b><br/>
서비스 해지를 희망하는 고객은 회사가 정한 소정의 절차(유무선인터넷 홈페이지 등을 통해 공지합니다)를 통해 서비스 해지를 신청할 수 있습니다.<br/>
<br/>

<b>제6조 (서비스의 수준)</b><br/>
① 서비스의 이용은 연중무휴 1일 24시간을 원칙으로 합니다. 단, 회사의 업무상이나 기술상의 이유로 서비스가 일시 중지될 수 있으며, 운영상의 목적으로 회사가 정한 기간에는 서비스가 일시 중지될 수 있습니다. 이러한 경우 회사는 사전 또는 사후에 이를 공지합니다.<br/>
② 위치정보는 관련 기술의 발전에 따라 오차가 발생할 수 있습니다.<br/>
<br/>

<b>제7조 (서비스 이용의 제한 및 정지)</b><br/>
회사는 고객이 다음 각 호에 해당하는 경우 사전 통지 없이 고객의 서비스 이용을 제한 또는 정지하거나 직권 해지를 할 수 있습니다.<br/>
1. 다른 사람의 명의(주민등록번호) 등을 이용하여 서비스에 가입한 경우<br/>
2. 타인의 서비스 이용을 방해하거나 타인의 개인정보를 도용한 경우<br/>
3. 서비스를 이용하여 법령, 공공질서, 미풍양속 등에 반하는 행위를 한 경우<br/>
<br/>

<b>제8조 (서비스의 변경 및 중지)</b><br/>
① 회사는 다음에 해당하는 경우 고객에게 서비스의 전부 또는 일부를 제한, 변경하거나 중지할 수 있습니다.<br/>
1. 서비스용 설비의 보수 등 공사로 인한 부득이한 경우<br/>
2. 정전, 제반 설비의 장애 또는 이용량의 폭주 등으로 정상적인 서비스 이용에 지장이 있는 경우<br/>
3. 서비스 제휴업체와의 계약 종료 등과 같은 회사의 제반 사정 또는 법률상의 장애 등으로 서비스를 유지할 수 없는 경우<br/>
4. 기타 천재지변, 국가비상사태 등 불가항력적 사유가 있는 경우<br/>
② 제1항에 의한 서비스 중단의 경우에는 회사는 인터넷 등에 공지하거나 고객에게 통지합니다. 다만, 회사가 통제할 수 없는 사유로 인한 서비스의 중단 (운영자의 고의, 과실이 없는 디스크 장애, 시스템다운 등)으로 인하여 사전 통지가 불가능한 경우에는 사후에 통지합니다.<br/>
<br/>

<b>제9조 (서비스 종류 및 이용요금)</b><br/>
회사가 제공하는 서비스의 종류와 세부내용 및 이용 요금은 별표<br/>
1. 서비스의 종류에 따릅니다.<br/>

